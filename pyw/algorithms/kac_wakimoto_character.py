"""
Kac-Wakimoto Character Formula for Admissible Modules

This module implements the Kazhdan-Lusztig character formula for computing
characters of admissible representations at fractional levels.

The main formula (Di Francesco Eq. 14.150):

    ch(L_λ) = (1/denominator) · Σ Q̃_{[w],[w']}(1) · e^{w'·Λ}

where:
    - L_λ is the irreducible module with highest weight λ
    - Q̃ are inverse Kazhdan-Lusztig polynomials evaluated at 1
    - The sum is over cosets [w'] in W_Λ/W^0_Λ with [w] ≤ [w'] in Bruhat order
    - w·Λ denotes the dot action: w·Λ = w(Λ + ρ̂) - ρ̂

Key concepts:
    - W_Λ: Stabilizer subgroup {w ∈ W | (Λ, α^∨) ∈ Z for all α in orbit}
    - W^0_Λ: Invariant subgroup {w ∈ W_Λ | w·Λ = Λ}
    - Cosets W_Λ/W^0_Λ parametrize the terms in the character formula

References:
    - Kac, V. G., Wakimoto, M. "On rationality of W-algebras"
    - Di Francesco et al., "Conformal Field Theory", Chapter 14
    - Cordova, Gaiotto, Shao "Infrared Computations of Defect Schur Indices"
"""

